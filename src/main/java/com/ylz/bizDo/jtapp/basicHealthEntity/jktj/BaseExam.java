public class BaseExam {
    private String height;//身高
    private String weight;//体重
    private String bmi;//体质指数
    private String waistline;//腰围
    private String temperature;//体温
    private String pulseRate;//脉率
    private String breathRate;//呼吸频率
    private String bpLsbp;//血压-左侧收缩压
    private String bpLdbp;//血压-左侧舒张压
    private String bpRsbp;//血压-右侧收缩压
    private String bpRdbp;//血压-右侧舒张压
    private String heartRate;//心率

    public String getHeight() {
        return height;
    }

    public void setHeight(String height) {
        this.height = height;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getBmi() {
        return bmi;
    }

    public void setBmi(String bmi) {
        this.bmi = bmi;
    }

    public String getWaistline() {
        return waistline;
    }

    public void setWaistline(String waistline) {
        this.waistline = waistline;
    }

    public String getTemperature() {
        return temperature;
    }

    public void setTemperature(String temperature) {
        this.temperature = temperature;
    }

    public String getPulseRate() {
        return pulseRate;
    }

    public void setPulseRate(String pulseRate) {
        this.pulseRate = pulseRate;
    }

    public String getBreathRate() {
        return breathRate;
    }

    public void setBreathRate(String breathRate) {
        this.breathRate = breathRate;
    }

    public String getBpLsbp() {
        return bpLsbp;
    }

    public void setBpLsbp(String bpLsbp) {
        this.bpLsbp = bpLsbp;
    }

    public String getBpLdbp() {
        return bpLdbp;
    }

    public void setBpLdbp(String bpLdbp) {
        this.bpLdbp = bpLdbp;
    }

    public String getBpRsbp() {
        return bpRsbp;
    }

    public void setBpRsbp(String bpRsbp) {
        this.bpRsbp = bpRsbp;
    }

    public String getBpRdbp() {
        return bpRdbp;
    }

    public void setBpRdbp(String bpRdbp) {
        this.bpRdbp = bpRdbp;
    }

    public String getHeartRate() {
        return heartRate;
    }

    public void setHeartRate(String heartRate) {
        this.heartRate = heartRate;
    }
}
